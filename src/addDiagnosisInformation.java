    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String patientID=jTextField1.getText();
        try{
        Connection con=ConnectioProvider.getCon();
        /*if(con==null)
        {
            System.out.print("ewe");
        }*/
        Statement stt=(Statement)con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
        ResultSet rs=stt.executeQuery("select * from patient where patientID='"+patientID+"'");
        jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        flag=1;
        if(!rs.first())
        {
            jLabel2.setVisible(true);
        }
        else{
            jLabel2.setVisible(false);
            jTextField1.setEditable(false);
            flag=1;
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    catch(Exception e){
       // System.out.print(e);
        JOptionPane.showMessageDialog(null, "Connection failed !!!");
    }
    }
    
