    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(flag==1)
        {
            String patientID=jTextField1.getText();
            String symptom=jTextField2.getText();
            String diagnosis=jTextField3.getText();
            String medicines=jTextField4.getText();
            String wardReq;
            String typeWard;
            if(jCheckBox1.isSelected())
            {
                wardReq="YES";
                typeWard=(String)jComboBox1.getSelectedItem();
            }
            else
            {
                wardReq="NO";
                 typeWard="";
            }
            try{
               Connection con  =ConnectioProvider.getCon();
               Statement se=con.createStatement();
               se.executeUpdate("insert into patientreport values('"+patientID+"','"+symptom+"','"+diagnosis+"','"+medicines+"','"+wardReq+"','"+typeWard+"')");
               JOptionPane.showMessageDialog(null,"Successfully updated !!!");
               setVisible(false);
               new addDiagnosisInformation().setVisible(true);
               
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, e);
            }
        
        }
        else
        {
            JOptionPane.showMessageDialog(null, "PatientID field is empty !!!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

